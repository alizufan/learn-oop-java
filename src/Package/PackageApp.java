package Package;

public class PackageApp {
  // # What is Package ?
  //   - When we create an application, we can be sure we will create a lot of classes.
  //   - If there are too many classes, sometimes it will be difficult for us to find or classify the types of classes.
  //   - Java has a package feature, which is a feature similar to a folder / directory, where we can store our classes in packages.
  //   - Just like folders / directories, packages can also be nested, we can use a period (.) to create a nested package.
  //   - When we save a class in a package, above the Java file, we must mention the name of the package.
  //
}
