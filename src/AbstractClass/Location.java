package AbstractClass;

// # What is Abstract Class ?
//   - When we create a class, we can make a class an abstract class.
//   - Abstract class means that this class cannot be created as an object directly, it can only be derived.
//   - To make a class abstract, we can use the abstract keyword before the class keyword.
//   - Thus we can use the abstract class as a contract for the child class.
//
public abstract class Location {
  String name;
}
