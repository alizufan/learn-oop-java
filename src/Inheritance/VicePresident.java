package Inheritance;

// # What is Inheritance ?
//   - Inheritance is the ability to pass on a class to another class
//   - In that sense, we can create a Parent class and a Child class.
//   - Child class, can only have one Parent class,
//     but one Parent class can have many Child classes.
//   - When a class is inherited,
//     all fields and methods in the Parent class will automatically be owned by
//     the Child class.
//   - To inherit, in the child class, we must use the extends keyword followed by
//     the name of the parent class.
//
